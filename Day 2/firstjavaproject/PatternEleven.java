package firstjavaproject;

public class PatternEleven {

	public static void main(String[] args) {
		
int n=5;
		
		int i,j;
		for(i=5; i>=1; i--) {
			
				for(j=5; j>=i; j--) {
					System.out.print(j+" ");
			}
			for(j=0; j<=n-i; j++) {
		}
		System.out.println(" ");
	}
	}

}

